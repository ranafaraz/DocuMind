"""Offline eval harness: benchmark extraction across doc types, with and without
the verifier, plus the scrambled-layout null test.

Run: ``python -m evals.harness`` (writes ``evals/RESULTS.md``).

Two effects are measured independently:

* **Layout vs. text** (verifier held on): geometry recovers field associations
  that reading order loses -- most starkly on the two-column ``form``.
* **Verifier on vs. off** (extractor held on ``layout``): arithmetic
  reconciliation repairs the OCR-corrupted total and lifts validity to 100%.

The null test scrambles every page's geometry and re-runs the layout extractor;
its field accuracy collapses, proving the geometry was load-bearing. No API keys,
no downloads.
"""

from __future__ import annotations

import json
from pathlib import Path

from documind.config import Settings
from documind.pipeline import run_document
from documind.schema import DOCTYPES
from evals.metrics import aggregate

N_SEEDS = 60
RESULTS_PATH = Path(__file__).parent / "RESULTS.md"

# (label, extractor, verify)
CONFIGS = [
    ("text", "text", False),
    ("text+verify", "text", True),
    ("layout", "layout", False),
    ("layout+verify", "layout", True),
]
CONFIG_LABELS = [c[0] for c in CONFIGS]


def _cfg(ablate: bool = False) -> Settings:
    cfg = Settings()
    cfg.ocr_noise = 0.15
    cfg.ablate_layout = ablate
    return cfg


def _run(extractor: str, verify: bool, ablate: bool = False):
    out = {dt: [] for dt in DOCTYPES}
    for dt in DOCTYPES:
        for seed in range(N_SEEDS):
            out[dt].append(run_document(dt, seed, _cfg(ablate), extractor, verify))
    return out


def run_eval() -> dict:
    by_config = {label: _run(ex, ve) for label, ex, ve in CONFIGS}
    overall = {
        label: aggregate([r for dt in DOCTYPES for r in by_config[label][dt]])
        for label in CONFIG_LABELS
    }
    per_domain = {
        dt: {label: aggregate(by_config[label][dt]) for label in CONFIG_LABELS}
        for dt in DOCTYPES
    }

    # Null test: layout extractor on scrambled geometry (verifier on).
    ablation_runs = _run("layout", True, ablate=True)
    ablation = aggregate([r for dt in DOCTYPES for r in ablation_runs[dt]])

    return {
        "config": {
            "doctypes": list(DOCTYPES),
            "seeds_per_doctype": N_SEEDS,
            "total_documents": N_SEEDS * len(DOCTYPES),
        },
        "overall": overall,
        "per_domain": per_domain,
        "ablation": ablation,
        # Flat scalars consumed by the gate.
        "layout_field_acc": overall["layout+verify"]["field_accuracy"],
        "text_field_acc": overall["text+verify"]["field_accuracy"],
        "form_layout_field_acc": per_domain["form"]["layout+verify"]["field_accuracy"],
        "form_text_field_acc": per_domain["form"]["text+verify"]["field_accuracy"],
        "receipt_layout_field_acc": per_domain["receipt"]["layout+verify"]["field_accuracy"],
        "receipt_text_field_acc": per_domain["receipt"]["text+verify"]["field_accuracy"],
        "layout_cell_f1": overall["layout+verify"]["cell_f1"],
        "text_cell_f1": overall["text+verify"]["cell_f1"],
        "validity_noverify": overall["layout"]["validity"],
        "validity_verify": overall["layout+verify"]["validity"],
        "total_acc_noverify": overall["layout"]["total_field_acc"],
        "total_acc_verify": overall["layout+verify"]["total_field_acc"],
        "ablation_field_acc": ablation["field_accuracy"],
    }


def _row(label: str, m: dict, bold: bool = False) -> str:
    b = "**" if bold else ""
    cell_f1 = f"{m['cell_f1']:.3f}" if m.get("cells") else "n/a"
    return (
        f"| {b}{label}{b} | {b}{m['field_accuracy']:.3f}{b} | {cell_f1} | "
        f"{m['doc_exact']:.3f} | {b}{m['validity']:.3f}{b} | {m['total_field_acc']:.3f} |"
    )


def _render_markdown(res: dict) -> str:
    c = res["config"]
    ov = res["overall"]
    ab = res["ablation"]
    lines = [
        "# DocuMind - Offline Evaluation Results",
        "",
        "Generated by `python -m evals.harness` over "
        f"{c['total_documents']} deterministic documents "
        f"({c['seeds_per_doctype']} seeds x {len(c['doctypes'])} doc types), no API keys, "
        "no downloads. Every document is scored against its **ground-truth record**.",
        "",
        "## Headline: four configurations (pooled across doc types)",
        "",
        "| Config | Field acc | Cell F1 | Doc exact | Validity | Total acc |",
        "|---|---:|---:|---:|---:|---:|",
        _row("text", ov["text"]),
        _row("text + verify", ov["text+verify"]),
        _row("layout", ov["layout"]),
        _row("layout + verify", ov["layout+verify"], bold=True),
        "",
        "Two effects, cleanly separated:",
        "",
        f"- **Layout geometry buys field-association accuracy.** Holding the verifier on, "
        f"switching the reading-order extractor for the layout-aware one lifts field accuracy "
        f"from {ov['text+verify']['field_accuracy']:.0%} to "
        f"{ov['layout+verify']['field_accuracy']:.0%} "
        f"-- almost all of the gap is the two-column `form`, where reading order interleaves "
        f"the columns (see per-doc-type table).",
        f"- **The schema verifier buys arithmetic validity.** Holding the extractor on `layout`, "
        f"turning the verifier on lifts record validity from {ov['layout']['validity']:.0%} to "
        f"{ov['layout+verify']['validity']:.0%} and `total` accuracy from "
        f"{ov['layout']['total_field_acc']:.0%} to {ov['layout+verify']['total_field_acc']:.0%}, "
        f"by reconciling the OCR-corrupted total against subtotal + tax.",
        "",
        "## Null test: scramble the page geometry, re-run the layout extractor",
        "",
        "| Config | Field acc | Cell F1 | Doc exact |",
        "|---|---:|---:|---:|",
        f"| layout + verify (real geometry) | {ov['layout+verify']['field_accuracy']:.3f} | "
        f"{ov['layout+verify']['cell_f1']:.3f} | {ov['layout+verify']['doc_exact']:.3f} |",
        f"| layout + verify (scrambled geometry) | {ab['field_accuracy']:.3f} | "
        f"{ab['cell_f1']:.3f} | {ab['doc_exact']:.3f} |",
        "",
        f"Destroy the layout and the geometry-based extractor collapses from "
        f"{ov['layout+verify']['field_accuracy']:.0%} to {ab['field_accuracy']:.0%} field accuracy "
        "-- confirming it was reading the page's geometry, not memorising text order.",
        "",
        "## Per-doc-type breakdown",
        "",
    ]
    for dt in res["config"]["doctypes"]:
        lines += [
            f"### {dt}",
            "",
            "| Config | Field acc | Cell F1 | Doc exact | Validity | Total acc |",
            "|---|---:|---:|---:|---:|---:|",
        ]
        for label in CONFIG_LABELS:
            lines.append(_row(label, res["per_domain"][dt][label]))
        lines.append("")
    return "\n".join(lines)


def main() -> int:
    res = run_eval()
    RESULTS_PATH.write_text(_render_markdown(res), encoding="utf-8")
    print("DocuMind eval complete. Wrote", RESULTS_PATH.name)
    summary = {"overall": res["overall"], "ablation": res["ablation"]}
    print(json.dumps(summary, indent=2))
    return 0


if __name__ == "__main__":
    raise SystemExit(main())
